//318A - Even odds

#include <cmath>
#include <cstdio>
#include <iostream>
using namespace std;

int main()
{
	int k, n;
	scanf("%d %d", &n, &k);

	if(k - ceil(double(n)/2) == 0)
				cout << n;
	else if(k <= n/2)
		cout << 2 * k - 1;
	else
		cout << (k - ceil(double(n)/2)) * 2;

	return 0;
}
